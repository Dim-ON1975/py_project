def summ_two(num1: int, num2: int) -> int:
    """
    Функция сложения двух чисел
    :param num1: целое число, int
    :param num2: целое число, int
    :return: сумма двух чисел, int
    """
    return num1 + num2


def diff_two(num1: int, num2: int) -> int:
    """
    Функция вычитания двух целых чисел
    :param num1: целое число, int
    :param num2: целое число, int
    :return: разность двух чисел, int
    """
    return num1 - num2
